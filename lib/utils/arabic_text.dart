/// أدوات تبسيط عرض النص القرآني.
///
/// الهدف: قراءة أوضح وأبسط بإزالة التشكيل/العلامات غير الضرورية،
/// مع الحفاظ التام على صحة النص.
///
/// ما يُزال (آمن بصرياً ولا يغيّر النطق):
///   • علامات الوقف العثمانية وعلامات الضبط الصغيرة (ۖ ۗ ۚ ۛ ۜ …)
///   • الألف الواصلة (ٱ) تُحوّل إلى ألف عادية (ا)
///
/// ما يبقى عمداً:
///   • الحركات الأساسية (فتحة/كسرة/ضمة/شدة/سكون/تنوين)
///   • الألف الخنجرية الصغيرة (ٰ) — إزالتها برمجياً تُفسد كلمات مثل
///     «صراط» و«هذا» و«الرحمن»، فتركها أسلم.
///   • علامات أرقام الآيات ﴿١﴾ والأرقام العربية.
String simplifyQuran(String input) {
  final sb = StringBuffer();
  for (final r in input.runes) {
    // الألف الواصلة ٱ → ا
    if (r == 0x0671) {
      sb.writeCharCode(0x0627);
      continue;
    }
    // علامات الضبط/الوقف القرآنية الصغيرة → حذف
    final isHonorific = r >= 0x0610 && r <= 0x061A; // علامات فوق السطر
    final isQuranicMark = r >= 0x06D6 && r <= 0x06ED; // علامات الوقف والضبط
    final isExtendedMark = r >= 0x08D3 && r <= 0x08FF; // Arabic Extended-A
    if (isHonorific || isQuranicMark || isExtendedMark) {
      continue;
    }
    sb.writeCharCode(r);
  }
  return sb.toString();
}
